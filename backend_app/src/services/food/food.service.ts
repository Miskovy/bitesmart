/**
 * Get all food items from local database with pagination and search
 */
export const getAllFoodDb = async ({
    page = 1,
    limit = 10,
    search,
}: {
    page?: number;
    limit?: number;
    search?: string;
}) => {
    const skip = (page - 1) * limit;

    const whereClause = search
        ? like(foodItems.class_name, `%${search}%`)
        : undefined;

    const totalResult = await db
        .select({ value: count() })
        .from(foodItems)
        .where(whereClause);

    const total = totalResult[0]?.value || 0;

    const foodList = await db.query.foodItems.findMany({
        where: whereClause,
        limit: limit,
        offset: skip,
        orderBy: [desc(foodItems.id)],
    });

    return {
        foodItems: foodList,
        total,
        page,
        limit,
    };
};

/**
 * Get single food item from local database by ID
 */
export const getFoodDbById = async (id: number) => {
    const food = await db.query.foodItems.findFirst({
        where: eq(foodItems.id, id),
    });

    if (!food) {
        throw new NotFound(`Food item with ID ${id} not found`);
    }

    return food;
};

/**
 * Create a new food item in the local database
 */
export const createFood = async (data: any) => {
    const { class_name, ...extraData } = data;

    if (!class_name) {
        throw new BadRequest("Food class name is required");
    }

    const existing = await db.query.foodItems.findFirst({
        where: eq(foodItems.class_name, class_name),
    });

    if (existing) {
        throw new BadRequest("Food item with this name already exists");
    }

    await db.insert(foodItems).values({
        class_name,
        ...extraData,
    });

    const newFood = await db.query.foodItems.findFirst({
        where: eq(foodItems.class_name, class_name),
    });

    if (!newFood) {
        throw new BadRequest("Failed to retrieve newly created food item");
    }

    return newFood;
};

/**
 * Update an existing food item in the local database
 */
export const updateFood = async (id: number, data: any) => {
    const food = await db.query.foodItems.findFirst({
        where: eq(foodItems.id, id),
    });

    if (!food) {
        throw new NotFound(`Food item with ID ${id} not found`);
    }

    const { class_name, ...updates } = data;

    if (class_name && class_name !== food.class_name) {
        const existing = await db.query.foodItems.findFirst({
            where: eq(foodItems.class_name, class_name),
        });

        if (existing) {
            throw new BadRequest("Food item name is already taken");
        }
        updates.class_name = class_name;
    }

    if (Object.keys(updates).length > 0) {
        await db.update(foodItems).set(updates).where(eq(foodItems.id, id));
    }

    const updatedFood = await db.query.foodItems.findFirst({
        where: eq(foodItems.id, id),
    });

    if (!updatedFood) {
        throw new BadRequest("Failed to retrieve updated food item");
    }

    return updatedFood;
};

/**
 * Delete a food item and clear referencing daily logs, recipe ingredients, and meal plan items
 */
export const deleteFood = async (id: number) => {
    const food = await db.query.foodItems.findFirst({
        where: eq(foodItems.id, id),
    });

    if (!food) {
        throw new NotFound(`Food item with ID ${id} not found`);
    }

    await db.transaction(async (tx) => {
        // 1. Delete dependent recipe ingredients
        await tx.delete(recipeIngredients).where(eq(recipeIngredients.foodItemId, id));

        // 2. Delete dependent meal plan items
        await tx.delete(mealPlanItems).where(eq(mealPlanItems.foodItemId, id));

        // 3. Delete daily logs referencing this food item
        await tx.delete(dailyLogs).where(eq(dailyLogs.foodItemId, id));

        // 4. Delete the food item
        await tx.delete(foodItems).where(eq(foodItems.id, id));
    });

    return { id, message: "Food item and all related records deleted successfully" };
};
